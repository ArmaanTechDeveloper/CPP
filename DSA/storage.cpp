#include <iostream>

using namespace std;

int main(){
    int a;
    cout<<"The size of int is : "<<sizeof(a)<<"bytes"<<endl;
    float b;
    cout<<"The size of float is : "<<sizeof(b)<<"bytes"<<endl;
    short int c;
    cout<<"The size of short int is : "<<sizeof(c)<<"bytes"<<endl;
    return 0;
}